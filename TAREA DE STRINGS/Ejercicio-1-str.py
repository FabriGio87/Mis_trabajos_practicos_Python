"""### Ejercicio 1: Longitud de cadenas y acceso por índice 
1. Declara una variable var1 con una cadena de texto de tu elección.
2. Imprime la longitud de la cadena utilizando la función len(). 
3. Accede al tercer carácter de la cadena e imprímelo."""
# 1. Declaramos la variable
var1 = "Programar es imaginar soluciones"
# Imprimimos la longitud de la cadena
print(f"La longitud de la cadena es: {len(var1)}")
# Para acceder al tercer carácter tenemos que contar desde 0 incluyendo espacios, es decir que el tercer carácter sería "var2".
print(f"El tercer carácter es: {var1[2]}")