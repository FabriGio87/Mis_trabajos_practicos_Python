"""### Ejercicio 1: Uso básico de `while` con contador
1. Escribe un programa que imprima los números del 1 al 5 usando un bucle `while`.
2. Utiliza un contador que comience en 1 y se incremente en cada iteración hasta llegar a 5."""

# Contador
contador = 1

while contador <= 5:
    print(contador)
    contador += 1