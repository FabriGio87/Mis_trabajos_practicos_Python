"""Ejercicio 3: Iterando sobre un rango de números
1. Escribe un programa que use un bucle `for` para imprimir los números del 1 al 9 (inclusive), usando `range()`.
2. En cada iteración, imprime el número junto con el mensaje "Este es el número X", donde `X` es el número actual."""

# Iterar del 1 al 9.
