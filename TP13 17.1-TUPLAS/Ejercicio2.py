"""### Ejercicio 2: Subtuplas (rebanado o slicing)
1. Crea una tupla llamada `valores` con al menos 6 elementos.
2. Extrae una subtupla desde el segundo elemento hasta el quinto (sin incluir el quinto).
3. Imprime la subtupla resultante. """

# 1. Creamos una tupla.
valores = (10, 20, 30, 40, 50, 60)

# 2. Extraer subtupla desde segundo elemento hasta quinto elemento (sin incluirlo).
subtupla = valores[1:4]

# 3. Imprimimos el resultado.
print("Subtupla:", subtupla)