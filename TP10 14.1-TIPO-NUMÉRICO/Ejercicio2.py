"""Ejercicio 2: Incrementadores y decrementadores
1. Declara una variable `num` con valor inicial 10.
2. Utiliza los operadores incrementadores `+=`, `-=`, `*=`, y `/=` para modificar el valor de `num` paso a paso. 
3. Imprime el valor de `num` después de cada operación."""

# 1. Declaramos una variable.
num = 13
print(f"Valor inicial: {num}")

# 2. Operaciones.
num += 2
print(f"Después de +=: {num}")

num -= 3
print(f"Después de -=: {num}")

num *= 4
print(f"Después de *=: {num}")

num /= 5
print(f"Después de /=: {num}")