"""Ejercicio 1: Operaciones básicas con enteros y decimales
1. Declara dos variables: un número entero `num1` y un número decimal `num2`.
2. Realiza las siguientes operaciones entre `num1` y `num2`: suma, resta, multiplicación, división, división entera, y módulo.
3. Imprime el resultado de cada operación."""

# 1. Declaramos las variables.
num1 = 10
num2 = 4.5

# 2. Operaciones
suma = num1 + num2
resta = num1 - num2
multiplicacion = num1 * num2
division = num1 / num2
division_entera = num1 // num2
modulo = num1 % num2

# 3. Imprimimos.
print(f"Suma: {suma}")
print(f"Resta: {resta}")
print(f"Multiplicación: {multiplicacion}")
print(f"División: {division}")
print(f"División entera: {division_entera}")
print(f"Módulo: {modulo}")